
def get_weather(city : str):
    if city == "California":
        return "sunny"
    if city == "Arizona":
        return "snowy"
    if city == "Paris":
        return "rainy"
    if city == "London":
        return "cloudy"
    if city == "New York":
        return "sunny"
    if city == "Toronto":
        return "rainy"
    
